			<h1>中心介绍</h1>
<pre>华中农业大学于2009年获批为MBA培养单位，属于第八批MBA培养院校。学校高度重视MBA项目的建设，坚持质量第一、规范管理、内涵发展的总体思路，致力于为我国中部地区中小企业和涉农企业培养高素质经营管理人才，取得了良好的办学效果。自2010年秋季MBA开始招生以来，截止2014年，累计招生282人，毕业148人，目前在校学生134人。
发展环境
（1）地域优势明显
近年来，随着中部崛起战略的推进和长江经济带战略的实施，占有得天独厚地理位置优势的湖北省经济发展势头强劲，作为长江中游城市群代表的武汉市更是呈现出日新月异的面貌，一直保持10%以上的经济发展速度，2014年国民生产总值已超过1万亿元，在副省级城市中位列第三。经济的高速发展催生了对高层次管理人才的旺盛需求。广大中小企业已成为湖北省、武汉市经济发展中最有活力的重要组成部分，具有强烈的MBA教育需求，因而提高中小企业企业家及经营管理者的管理素质，为中小企业的发展提供智力支持具有较广阔的市场空间。华中农业大学作为在全国具有较高影响力的高校，具有较长的中小企业管理教学科研历史，地处武汉且能辐射华中各省，以中小企业管理作为MBA教育的主要培养方向具有一定优势。
（2）学校高度重视
自MBA教育开办以来，学校从多方面予以支持。一是组织保障，MBA教育开办以来，学校成立了由分管校长任组长的MBA教育领导小组、MBA教学指导委员会以及华中农业大学MBA教育中心。在研究生院的领导下，MBA教育中心依托经济管理学院对MBA招生、培养等环节进行统一管理。二是政策倾斜，建立了师资培养、教学激励等制度，支持MBA任课教师队伍建设，鼓励教师不断提升教学效果，并将MBA教研成果等作为职称评审与岗位考核的重要参考依据；三是经费支持，将70%的MBA学费留在学院发展MBA教育；安排1500平方米场地用于MBA教学和办公，先期投入300多万元用于MBA教学软硬件建设；后期为MBA课程建设、教学研究、师资培养和案例开发等提供每年50万元专项资金支持。
（3）办学力量雄厚
华中农业大学深厚的历史积淀为华中农业大学MBA教育注入了精神和灵魂，学校在农林经济管理、工商管理、法学、经济学等学科的雄厚办学实力为MBA教育发展提供了坚实的基础。经济管理学院现有农林经济管理一级学科博士点1个、农林经济管理博士后科研流动站1个，农村中小企业管理、农业贸易与农村金融管理、农业经济管理、林业经济管理等博士学位授权专业4个，工商管理、农林经济管理一级学科硕士点2个，企业管理、国际贸易学、产业经济学二级学科硕士点3个，建有湖北省人文社科重点研究基地1个，实验经济学与空间经济学实验室各1个，具有本科、硕士、博士及博士后完整的经管类人才培养体系，与英国哈珀亚当斯大学联合培养营销学本科生。学院现有专任教师90名，其中教授19人，副教授40人，博士生导师23人，国家现代农业产业技术体系经济学岗位科学家5人，国务院学位委员会学科评议组成员1名，“新世纪百千万人才工程”国家级人选1名，享受国务院政府特殊津贴专家1名，教育部教学指导委员会委员2名，教育部新世纪优秀人才支持计划6名，教育部高校青年教师奖获得者1名，霍英东青年教师奖获得者1名，省部级有突出贡献中青年专家3名，享受湖北省政府专项津贴人员1名，湖北省跨世纪学科带头人2名，湖北省“楚天学者”讲座教授2名，入选湖北省百人计划1名，湖北省新世纪高层次人才工程3名。
（4）生源竞争激烈
在华中农业大学MBA教育开办之初，湖北省已有6所MBA培养学校，第八、九批又有9所学校获得MBA培养资格，至今湖北省具备MBA培养资格的院校高达15所，其中13所在武汉市。培养历史较久的武汉大学、华中科技大学、中南财经政法大学三所高校占据了大部分生源份额。华中农业大学仅有5年MBA办学经验，与有多年MBA办学经验的院校相比，虽然我校MBA项目的社会知名度和办学经验还存在一定差距，但是依靠错位发展的竞争战略和严格的培养质量管理，我校MBA教育已在中部地区形成良好口碑并占有稳定的一席之地。
发展思路
（1）突出特色，错位发展
华中农业大学MBA教育始终坚持突出特色、错位发展的思路，聚焦于为中小企业及涉农企业培养高层次管理人才。具体体现在招生、课程设置、教学内容、教学实习等方面。生源方面，我校MBA研究生60%以上来源于中小企业或涉农企业；课程设置方面，根据中小企业管理的需求，在选修课中开设了《领导科学与艺术》、《商法》、《企业社会责任》、《创业管理》等旨在提高学生自主创业能力的课程以及《农产品营销》、《现代生物技术》等涉农课程；教学内容方面，鼓励任课教师授课时多采用中小企业和涉农企业管理案例；教学实习方面，建立了“两江流域”（长江和汉江流域）教学科研基地，以水禽、柑橘、油菜、蔬菜、食用菌领域的5位产业经济学家承担的国家现代农业产业技术体系建设工作为平台,组织MBA研究生参与中小企业管理实践。
（2）加大投入，夯实基础
近五年来，学校和学院采取“多予、少取、放活”的方针，在师资队伍、办学条件、制度建设等方面加大投入，夯实了MBA办学基础。一是加强MBA师资队伍建设，每年拨出专款40万元用于MBA师资培训和案例开发。积极组织任课教师参加了历次由全国MBA教学指导委员会主办的师资培训，至2014年12月，100%的任课教师接受过MBA教学培训；MBA教育中心通过课题立项的方式鼓励任课教师进行案例开发，已资助案例开发项目4批18项，完成自主开发案例14项，其中两篇案例获全国“百篇优秀管理案例”奖。二是加强MBA教学的软硬件条件建设，在办学之初先期投入300万元建立了MBA专用图书室、MBA教学实习基地、MBA教学实验室、MBA专用教室、MBA专用案例讨论室等、购买了国泰安、创业之星、用友ERP等数据库和教学软件；MBA招生后，学校和学院每年共同投入50万元进一步改善办学条件，安排经费40万元用于MBA教师出国进修。三是加强MBA制度建设，组织专人在学习和借鉴兄弟院校经验的基础上建立了MBA管理制度体系并根据办学实践不断完善。
（3）依照标准，规范办学
在MBA教育领导小组和MBA教学指导委员会的指导下，参照兄弟院校的办学经验，按照MBA的办学规律及标准，制定了相关管理制度20余项，不断完善华中农业大学MBA教学管理体系和流程并严格执行，使我校MBA教育逐步步入规范化的轨道，确保了人才培养质量。MBA教学管理体系涵盖招生、教学、实践、毕业论文等多个环节，涉及学籍、考勤、奖惩、评教等多个方面。MBA人才培养流程包括了报到、注册、选课、开题、中期检查、毕业答辩等从入校到毕业的各个关键活动，规定了每个关键活动的时间节点和具体要求。如制定并严格执行《华中农业大学MBA学位论文规范》，实行严格的论文三审机制，每年因评审不合格而不能参加答辩的论文占提交论文总数的20%左右。
（4）追求卓越，质量至上
质量是MBA人才培养最重要的目标和考核标准。为了提高MBA人才培养质量，华中农业大学MBA教育中心从以下几个方面严加管理：一是严格任课教师和指导教师的选聘，任课教师和指导教师的选聘由MBA教学指导委员会在申请教师中按照规定条件进行遴选，并实施动态调整，学生满意度居于后10%的教师停止MBA教学资格一年，学生论文评价较低的指导老师停招MBA学生一年；二是通过走出去、请进来的方式鼓励MBA学生参与各种管理实践活动，办学5年来，坚持每年举办学术活动周，邀请人福科技王学海等企业家进入MBA课堂近50人次，组织MBA学生参加创业计划大赛、案例分析大赛、企业竞争模拟大赛等赛事；三是严把毕业论文关，MBA毕业论文除按规定抽取一定比例的盲评外，所有论文均需通过由MBA教育中心组织的三轮评审，合格后方可参加答辩，保证了MBA论文质量，所有MBA论文均顺利通过学校专业学位评定委员会审核。</pre>
