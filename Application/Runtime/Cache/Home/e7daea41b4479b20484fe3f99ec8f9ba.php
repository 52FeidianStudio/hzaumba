			<h1>招生信息</h1>
<pre>华中农业大学是一所以农科为优势，以生命科学为特色，理、工、农、文、法、经、管等学科相结合的教育部直属全国重点大学，是“211工程”重点建设高校。校园位于武汉市南湖狮子山，占地面积7425亩（约5平方公里），校园三面环水，背靠青山，风景秀丽，环境幽雅，是理想的学习和科研园地。2009年经教育部和国务院学位委员会批准，我校获得全日制MBA教育试点单位资格，2010年起可面向全国招生。
华中农业大学MBA教育中心依托于经济管理学院，该院师资力量雄厚，现有专任教师113人，其中博士生导师13人，教授20人，副教授35人，具有博士学位教师72人，聘请校外著名学者担任兼职教师36人；学院学科优势突出，现有农业经济管理、土地资源管理等博士学位授权点6个，企业管理、国际贸易等硕士学位授权点7个，其中农业经济管理二级学科为国家重点学科和湖北省优势学科，农林经济管理一级学科和土地资源管理二级学科为湖北省重点学科；学院科研实力强劲，下设农业经济管理系、土地管理系等6个系部，建有农林经济管理博士后科研流动站，设有湖北农村发展研究中心等10个研究机构及农业部经管总站华中农业大学农经服务中心，建有经济管理教学实验中心和土地资源管理教学实验中心，下设8个综合实验室。

攻读华中农业大学工商管理硕士（MBA），将是您事业成功的起点！

一、MBA报考条件：
1．拥护中国共产党的领导，勤奋学习，愿意为社会主义现代化建设服务，品德良好，遵纪守法。
2．大学本科毕业后有3年或3年以上工作经历；大专毕业后有5年或5年以上工作经历；已获硕士学位或博士学位并有2年或2年以上工作经历。
3. 身体健康状况符合规定的体检标准。
二、报考方式
2011年全国硕士研究生招生考试报名仍采用网上报名与现场确认相结合的方式。所有考生在网上报名后均需在规定时间到报考点进行现场确认后报名信息方为有效。
1．网报：考生须通过教育部指定网站（教育网址：http://yz.chsi.cn，公网网址：http://yz.chsi.com.cn）完成网上报名程序。时间为2010年10月10日—10月30日。（以教育部公布时间为准，逾期不再补报，也不得再修改报名信息。）
2．现场确认：网报成功后考生本人需到选择的报考点进行现场确认报考信息、缴费、照像。（2010年11月10日—14日）
3．现场工作流程：
(1)验证→(2)缴费→(3)照相→(4)领取“信息卡”并校对签名→(5)交回“信息卡”→(6)报名结束
未到现场缴费、照像、确认报名信息的考生，网报信息无效。
三、注意事项
1．考生在报名前请仔细核对本人条件是否符合报考要求，报考资格审查将在复试阶段进行。凡不符合报考条件的考生不予复试、录取。
2．请考生仔细核对报名信息，报名信息为录取信息，姓名、身份证号、信息栏内选择的报考点、招生单位、专业等不能修改。考生自行承担以上失误造成的后果。
3．考生选择报考点时要防止点击错误,一旦选定报考点就必须到该报考点缴费、照像、确认报名信息、参加考试。
4．2011年1月1日-14日，考生凭网报用户名和密码登录中国研究生招生信息网（http://yz.chsi.com.cn/）下载打印《准考证》，招生单位不再给考生寄发《准考证》。
    考生凭下载打印的《准考证》和本人有效身份证件参加初试。
5．考生网报期间如需帮助，请致电0278728639。
四、考试
考试分初试和复试两个阶段。
1．初试：
（1）初试时间：2011年1月15日、16日，具体时间和考场见《准考证》。
（2）考试科目：MBA联考英语、综合（数学、写作、逻辑）。
（3）考试地点：考生报考点。
2．复试：
（1）复试时间：预计在2011年4月。
（2）复试内容：
①外语听力口语测试
②以专业课笔试、综合面试等形式对考生的学科背景、基本素质、操作技能、思维能力、创新能力等进行考察。
五、录取方式
根据初试、复试成绩和对考生科研潜力、综合素质的考察结果，全面衡量考生各方面的表现，择优录取。
六、培养
1．学习方式：委托培养，周末上课或集中上课。
2．学习年限：一般为2年。
3．培养方向：中小企业管理、营销管理、供应链管理与食品安全控制、人力资源开发与管理。
七、毕业及证书发放
学习成绩合格，完成相关培养环节，通过学位论文答辩，经校学位评定委员会批准，授予工商管理硕士学位，同时颁发华中农业大学硕士研究生毕业证书。
